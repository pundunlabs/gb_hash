%%--------------------------------------------------------------------
%% @doc
%% Create a hash ring with SHA algorithm and uniform strategy.
%% @end
%%--------------------------------------------------------------------
create_ring_sha_uniform(_Config) -> 
    Nodes = [ lists:concat(["node",X]) || X <- lists:seq(0,15)],
    Algorithm = {algorithm, sha},
    Strategy = {strategy, uniform},
    Options = [Algorithm, Strategy],
    ?assertEqual(ok, gb_hash:create_ring("test_ring", Nodes, Options)).

%%--------------------------------------------------------------------
%% @doc
%% Create a hash ring with wrapper() algorithm and timedivision strategy.
%% @end
%%--------------------------------------------------------------------
create_ring_wrapper_timedivision(_Config) -> 
    Name = "test_ring_tda",
    Nodes = [ lists:concat(["node",X]) || X <- lists:seq(0,15)],
    FileMargin = length(Nodes),
    TimeMargin = 30,
    Algorithm = {algorithm, {tda, FileMargin, TimeMargin}},
    Strategy = {strategy, timedivision},
    Options = [Algorithm, Strategy],
    ?assertEqual(ok, gb_hash:create_ring(Name, Nodes, Options)),
    TestNodes = gb_hash:get_nodes(Name),
    ct:log("Created nodes: ~p~n",[TestNodes]),
    {Macros, RefSec, Micros} = os:timestamp(),
    KeyList = [begin Ts = {Macros, RefSec + (TimeMargin*N), Micros},
		     [{ts, Ts}]
	       end || N <- lists:seq(0, FileMargin-1)],
    [begin
	ExampleNode = gb_hash:get_node(Name, Key),
	ct:log("ExampleNode: ~p~n",[ExampleNode])
     end || Key <- KeyList].

%%--------------------------------------------------------------------
%% @doc
%% Delete hash rings created in this suite.
%% @end
%%--------------------------------------------------------------------
delete_rings(_Config) ->
    Rings = ["test_ring", "test_ring_tda"],
    [ ok = gb_hash:delete_ring(R) || R <- Rings ],
    ct:log("All rings deleted.").
